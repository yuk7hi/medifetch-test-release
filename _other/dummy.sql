    pharmacyId integer(20) NOT NULL AUTO_INCREMENT,
    name varchar(20),
    address varchar(30),
    telNo integer(15),
    email varchar(50),
    pass varchar(15),
    ownername varchar(20),
    openHrs varchar(20),
    partnership varchar(5),
    glink varchar(200),
    l1 decimal(11,6),
    l2 decimal(11,6),
